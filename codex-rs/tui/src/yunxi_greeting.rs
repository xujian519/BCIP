//! 云熙启动问候语：每次打开随机一条，活泼但不失专业。

use rand::Rng;

const YUNXI_GREETINGS: &[&str] = &[
    "嗨～我是云熙，专利代理人的智能搭档。今天想先从哪件事开始？",
    "你好呀，云熙已就绪。检索、撰写、答复审查意见——有事儿随时叫我～",
    "云熙上线！陪你处理专利事务，尽管吩咐～",
    "又见面啦～我是云熙，你的专利助手。有什么需要帮忙的？",
    "嘿，云熙在这里。无论是权利要求还是审查意见，咱们一起搞定～",
    "欢迎回来！我是云熙，专注专利代理工作。今天有什么计划？",
    "早安～云熙已就位。新申请、答复、检索，随时开始！",
    "下午好～我是云熙，专利智能助手。需要我帮你做什么？",
    "晚上好～云熙还在线。加班处理专利事务的话，我陪你～",
    "嗨，云熙报到！专利检索、文本撰写、案件分析——交给我就好～",
    "你好！我是云熙，知识产权领域的专属助手。今天一起推进哪项工作？",
    "云熙已准备好啦～有新案要撰写，还是审查意见要答复？",
    "来啦～我是云熙。权利要求布局、说明书润色、对比文件分析，我都可以～",
    "你好呀～云熙随时待命。专利代理人的日常事务，交给云熙就好～",
    "嗨，又见面了！今天有云熙陪你，专利工作轻松一点～",
    "云熙在此～检索现有技术、梳理技术特征、打磨答复策略，咱们开干吧！",
    "你好！云熙已上线。有什么专利难题，尽管抛过来～",
    "嗨～云熙准备好了。今天是要写新申请，还是处理审查意见？",
];

/// 随机选取一条云熙启动问候语。
pub(crate) fn pick_greeting() -> String {
    let mut rng = rand::rng();
    let index = rng.random_range(0..YUNXI_GREETINGS.len());
    YUNXI_GREETINGS[index].to_string()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn pick_greeting_returns_non_empty_message_from_pool() {
        let greeting = pick_greeting();
        assert!(YUNXI_GREETINGS.contains(&greeting.as_str()));
    }
}
