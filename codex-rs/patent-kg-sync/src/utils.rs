/// 截断字符串到最大字节长度（尊重 UTF-8 字符边界）
pub fn truncate(s: &str, max: usize) -> String {
    if s.len() <= max {
        s.trim().to_string()
    } else {
        let mut end = max;
        while !s.is_char_boundary(end) && end > 0 {
            end -= 1;
        }
        s[..end].trim().to_string()
    }
}

