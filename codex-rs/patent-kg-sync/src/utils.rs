mod tests {
    use super::*;

    #[test]
    fn test_chinese_to_number() {
        assert_eq!(chinese_to_number("二十二"), 22);
        assert_eq!(chinese_to_number("三"), 3);
        assert_eq!(chinese_to_number("46"), 46);
        assert_eq!(chinese_to_number("二十六"), 26);
        assert_eq!(chinese_to_number("四十六"), 46);
    }

    #[test]
    fn test_truncate() {
        assert_eq!(truncate("hello world", 5), "hello");
        assert_eq!(truncate("你好世界", 100), "你好世界");
    }
}
